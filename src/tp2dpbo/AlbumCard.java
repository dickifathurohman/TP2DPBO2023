    public AlbumCard(MainFrame panelCard, int id) {
        initComponents();
        ImageIcon icon = null;
        
        // Make connection to db
        db = new dbConnection();
        
        this.panelCard = panelCard;
        this.id = id;
        
        ResultSet res = db.selectQuery("SELECT * FROM album WHERE id_album=" +this.id);
        
        try {
            if(res.next()){
                labelTitle.setText(res.getString("title"));
                labelYear.setText(Integer.toString(res.getInt("year")));
                id_artist = res.getInt("artist_id");

                icon = new ImageIcon("src\\Assets\\"+res.getString("cover"));
                Image image = icon.getImage().getScaledInstance(100, 100, Image.SCALE_SMOOTH);
                ImageIcon ic = new ImageIcon(image);
                coverpic.setIcon(ic);
            }
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, e);
        }
        
        ResultSet artist = db.selectQuery("SELECT * FROM artist WHERE id=" + id_artist);
        
        try{
            if(artist.next()){
                labelArtist.setText(artist.getString("name"));
            }
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, e);
        }
    }

