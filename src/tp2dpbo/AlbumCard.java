    private void BdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BdeleteActionPerformed
        // TODO add your handling code here:
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION){
                String sql = "DELETE FROM album WHERE id_album=" +this.id;
                int cardDeleted = db.updateQuery(sql);
                if(cardDeleted > 0){
                    panelCard.setPanelAlbum();
                    JOptionPane.showMessageDialog(null, "Data telah dihapus!");
                }
                else{
                    JOptionPane.showMessageDialog(null, "Data gagal dihapus!");
                }
            } else{
                // aksi ketika pengguna memilih tombol No
            }
        
    }//GEN-LAST:event_BdeleteActionPerformed

